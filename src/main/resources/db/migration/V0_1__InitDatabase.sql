CREATE TABLE IF NOT EXISTS PUBLIC.IMAGE (
  ID VARCHAR(1000),
  TITLE VARCHAR(1000),
  DESCRIPTION TEXT,
  DATETIME BIGINT,
  TYPE VARCHAR(1000),
  ANIMATED BOOLEAN,
  WIDTH INT,
  HEIGHT INT,
  SIZE INT,
  VIEWS INT,
  BANDWIDTH BIGINT,
  DELETEHASH VARCHAR(1000),
  LINK VARCHAR(1000),
  GIFV VARCHAR(1000),
  MP4 VARCHAR(1000),
  MP4_SIZE INT,
  LOOPING BOOLEAN,
  VOTE VARCHAR(1000),
  FAVORITE BOOLEAN,
  NSFW BOOLEAN,
  COMMENT_COUNT INT,
  TOPIC VARCHAR(1000),
  TOPIC_ID BIGINT,
  SECTION VARCHAR_IGNORECASE(1000),
  ACCOUNT_URL VARCHAR(1000),
  ACCOUNT_ID BIGINT,
  UPS INT,
  DOWNS INT,
  POINTS INT,
  SCORE INT,
  IS_ALBUM BOOLEAN,
  IN_MOST_VIRAL BOOLEAN,
  BOT_SCORE INT DEFAULT(0),
  CONSTRAINT PK_IMAGE PRIMARY KEY (ID)
);

CREATE INDEX IDX_IMAGE_BOT_SCORE ON IMAGE( SECTION, BOT_SCORE DESC );
